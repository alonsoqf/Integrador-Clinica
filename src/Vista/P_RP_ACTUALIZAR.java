    private void btnActualizarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnActualizarMouseClicked
        // Obtener los nuevos valores de los campos de texto
        String idPaciente = txtID.getText();
        String nombres = txtNombres.getText();
        String apellidoPaterno = txtApellPaterno.getText();
        String apellidoMaterno = txtApellMaterno.getText();
        String dni = txtDNI.getText();
        // Obtener los nuevos valores para los demás campos de tu formulario
        Date fechaNacimiento = txtFechaNac.getDate();
        String genero = txtGenero.getSelectedItem().toString();
        String telefono = txtTelefono.getText();
        String correo = txtCorreo.getText();
        String estadoCivil = txtEstado.getSelectedItem().toString();
        String direccion = txtDireccion.getText();
        String nacionalidad = txtNacionalidad.getText();
        String celularEmergencia = txtCelEmergencia.getText();

        // Consulta SQL para actualizar el registro en la base de datos
        String consultaSql = "UPDATE registro_pacientes SET nombres = ?, apellido_paterno = ?, apellido_materno = ?, dni = ?, fech_nac = ?, genero = ?, telefono = ?, correo = ?, estado = ?, direccion = ?, nacionalidad = ?, celu_emergencia = ? WHERE id_paciente = ?";

        try {
            PreparedStatement ps = cn.prepareStatement(consultaSql);
            ps.setString(1, nombres);
            ps.setString(2, apellidoPaterno);
            ps.setString(3, apellidoMaterno);
            ps.setString(4, dni);
            ps.setDate(5, new java.sql.Date(fechaNacimiento.getTime()));
            ps.setString(6, genero);
            ps.setString(7, telefono);
            ps.setString(8, correo);
            ps.setString(9, estadoCivil);
            ps.setString(10, direccion);
            ps.setString(11, nacionalidad);
            ps.setString(12, celularEmergencia);
            // Establecer el parámetro para el ID del paciente (asumiendo que es el último parámetro en la consulta SQL)
            ps.setString(13, idPaciente);

            // Ejecutar la consulta SQL de actualización
            int filasActualizadas = ps.executeUpdate();

            // Verificar si se actualizaron filas en la base de datos
            if (filasActualizadas > 0) {
                JOptionPane.showMessageDialog(null, "Registro actualizado correctamente");
            } else {
                JOptionPane.showMessageDialog(null, "No se pudo actualizar el registro");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al actualizar el registro en la base de datos: " + e.getMessage());
        }
    }//GEN-LAST:event_btnActualizarMouseClicked

