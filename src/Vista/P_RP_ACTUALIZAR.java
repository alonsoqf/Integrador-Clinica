    private void btnBusquedaPacientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnBusquedaPacientesMouseClicked
        // Crear un JDialog para la ventana de búsqueda
        JDialog dialogo = new JDialog((Frame) SwingUtilities.getWindowAncestor(this), "Ventana de Búsqueda Pacientes", true);

        // Crear la tabla para mostrar los datos de registro de pacientes
        JTable tabla = new JTable();
        JScrollPane scrollPane = new JScrollPane(tabla);
        dialogo.add(scrollPane, BorderLayout.CENTER);

        // Crear un modelo de tabla
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID Paciente");
        modelo.addColumn("Nombres");
        modelo.addColumn("Apellido Paterno");
        modelo.addColumn("Apellido Materno");
        modelo.addColumn("DNI/PASAPORTE");
        modelo.addColumn("Fech.Nacimiento");
        modelo.addColumn("Genero");
        modelo.addColumn("Telefono");
        modelo.addColumn("Correo Electronico");
        modelo.addColumn("Estado Civil");
        modelo.addColumn("Direccion");
        modelo.addColumn("Nacionalidad");
        modelo.addColumn("Celular Emergencia");

        tabla.setModel(modelo);

        // Crear un JTextField para la búsqueda por DNI
        JTextField txtBDNI = new JTextField(20);
        JPanel panelBusqueda = new JPanel();
        panelBusqueda.add(new JLabel("Buscar por DNI:"));
        panelBusqueda.add(txtBDNI);
        dialogo.add(panelBusqueda, BorderLayout.NORTH);

        // Método para llenar la tabla con datos desde la base de datos
        Runnable cargarDatos = () -> {
            modelo.setRowCount(0); // Limpiar la tabla
            try {
                String consultaSql = "SELECT * FROM registro_pacientes";
                Statement st = cn.createStatement();
                java.sql.ResultSet rs = st.executeQuery(consultaSql);
                while (rs.next()) {
                    Object[] fila = new Object[13];
                    for (int i = 0; i < 13; i++) {
                        fila[i] = rs.getObject(i + 1);
                    }
                    modelo.addRow(fila);
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Error al cargar los datos de la base de datos: " + e.getMessage());
            }
        };

        // Cargar los datos inicialmente
        cargarDatos.run();

        // Configurar KeyListener para la búsqueda por DNI
        txtBDNI.addKeyListener(new KeyAdapter() {
            public void keyReleased(KeyEvent e) {
                String dni = txtBDNI.getText();
                TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(modelo);
                tabla.setRowSorter(sorter);
                sorter.setRowFilter(RowFilter.regexFilter(dni, 4)); // Filtrar por la columna DNI
            }
        });

        // Configurar el tamaño y la ubicación del JDialog
        dialogo.setSize(850, 450);
        dialogo.setLocationRelativeTo(this);

        // Agregar botones "Agregar" y "Salir"
        JButton btnAgregar = new JButton("Agregar");
        JButton btnSalir = new JButton("Salir");

        // Configurar ActionListener para el botón "Agregar"
        btnAgregar.addActionListener(new ActionListener() {
            public void actionPerformed(ActionEvent e) {
                // Obtener la fila seleccionada
                int filaSeleccionada = tabla.getSelectedRow();
                if (filaSeleccionada != -1) {
                    // Obtener los datos de la fila seleccionada
                    Object[] fila = new Object[modelo.getColumnCount()];
                    for (int i = 0; i < fila.length; i++) {
                        fila[i] = modelo.getValueAt(tabla.convertRowIndexToModel(filaSeleccionada), i);
                    }
                    // Mostrar los datos en los JTextField correspondientes del JPanel
                    txtID.setText(fila[0].toString());
                    txtNombres.setText(fila[1].toString());
                    txtApellPaterno.setText(fila[2].toString());
                    txtApellMaterno.setText(fila[3].toString());
                    txtDNI.setText(fila[4].toString());

                    if (fila[5] != null && !fila[5].toString().isEmpty()) {
                        try {
                            // Convertir la fecha de nacimiento de String a Date
                            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd"); // Ajustar el formato
                            Date fechaNacimiento = dateFormat.parse(fila[5].toString());

                            // Establecer la fecha de nacimiento en el JDateChooser
                            txtFechaNac.setDate(fechaNacimiento);
                        } catch (ParseException ex) {
                            // Manejar el error en caso de que la conversión falle
                            System.out.println("Error al convertir la fecha de nacimiento: " + ex.getMessage());
                        }
                    } else {
                        // Manejar el caso de valor nulo o vacío
                        txtFechaNac.setDate(null);
                    }

                    txtGenero.setSelectedItem(fila[6].toString());
                    txtTelefono.setText(fila[7].toString());
                    txtCorreo.setText(fila[8].toString());
                    txtEstado.setSelectedItem(fila[9].toString());
                    txtDireccion.setText(fila[10].toString());
                    txtNacionalidad.setText(fila[11].toString());
                    txtCelEmergencia.setText(fila[12].toString());
                } else {
                    JOptionPane.showMessageDialog(null, "Por favor, seleccione una fila para agregar.");
                }
            }
        });

        // Configurar ActionListener para el botón "Salir"
        btnSalir.addActionListener(new ActionListener() {
            public void actionPerformed(ActionEvent e) {
                dialogo.dispose(); // Cerrar el JDialog
            }
        });

        // Crear un panel para contener los botones
        JPanel panelBotones = new JPanel();
        panelBotones.add(btnAgregar);
        panelBotones.add(btnSalir);

        // Agregar el panel de botones al JDialog
        dialogo.add(panelBotones, BorderLayout.SOUTH);

        dialogo.setVisible(true); // Mostrar el JDialog
    }//GEN-LAST:event_btnBusquedaPacientesMouseClicked

