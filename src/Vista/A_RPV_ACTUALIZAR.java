    private void btnBusquedaDoctorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnBusquedaDoctorMouseClicked
        // Crear un JDialog para la ventana de búsqueda
        JDialog dialogo = new JDialog((Frame) SwingUtilities.getWindowAncestor(this), "Ventana de Búsqueda de Doctores", true);

        // Crear la tabla para mostrar los datos de registro de doctores
        JTable tabla = new JTable();
        JScrollPane scrollPane = new JScrollPane(tabla);
        dialogo.add(scrollPane, BorderLayout.CENTER);

        // Crear un modelo de tabla
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID Proveedor");
        modelo.addColumn("Codigo Proveedor");
        modelo.addColumn("Empresa");
        modelo.addColumn("RUC");
        modelo.addColumn("Teléfono");
        modelo.addColumn("Correo");
        modelo.addColumn("Imagen");

        tabla.setModel(modelo);

        // Método para llenar la tabla con datos desde la base de datos
        Runnable cargarDatos = () -> {
            modelo.setRowCount(0); // Limpiar la tabla
            try {
                String consultaSql = "SELECT * FROM registro_proveedor";
                Statement st = cn.createStatement();
                java.sql.ResultSet rs = st.executeQuery(consultaSql);
                while (rs.next()) {
                    Object[] fila = new Object[7];
                    for (int i = 0; i < 7; i++) {
                        fila[i] = rs.getObject(i + 1);
                    }
                    modelo.addRow(fila);
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Error al cargar los datos de la base de datos: " + e.getMessage());
            }
        };

        // Cargar los datos inicialmente
        cargarDatos.run();

        // Configurar el tamaño y la ubicación del JDialog
        dialogo.setSize(1000, 600);
        dialogo.setLocationRelativeTo(this);

        // Agregar botones "Agregar" y "Salir"
        JButton btnAgregar = new JButton("Agregar");
        JButton btnSalir = new JButton("Salir");

        // Configurar ActionListener para el botón "Agregar"
        btnAgregar.addActionListener(new ActionListener() {
            public void actionPerformed(ActionEvent e) {
                // Obtener la fila seleccionada
                int filaSeleccionada = tabla.getSelectedRow();
                if (filaSeleccionada != -1) {
                    // Obtener los datos de la fila seleccionada
                    Object[] fila = new Object[modelo.getColumnCount()];
                    for (int i = 0; i < fila.length; i++) {
                        fila[i] = modelo.getValueAt(tabla.convertRowIndexToModel(filaSeleccionada), i);
                    }
                    // Mostrar los datos en los JTextField correspondientes del JPanel
                    txtID.setText(fila[0].toString());
                    txtCodigo.setText(fila[1].toString());
                    txtEmpresa.setText(fila[2].toString());
                    txtRuc.setText(fila[3].toString());
                    txtCorreo.setText(fila[5].toString());
                    txtTelefono.setText(fila[4].toString());

                    // Procesar Imagen
                    if (fila[6] != null) {
                        try {
                            byte[] imageBytes = (byte[]) fila[6]; // Obtener el arreglo de bytes desde la fila
                            if (imageBytes.length > 0) {
                                InputStream is = new ByteArrayInputStream(imageBytes);
                                BufferedImage bufferedImage = ImageIO.read(is);
                                if (bufferedImage != null) {
                                    ImageIcon imageIcon = new ImageIcon(bufferedImage.getScaledInstance(290, 370, Image.SCALE_DEFAULT));
                                    JLabel imageLabel = new JLabel(imageIcon);
                                    panelimagen.removeAll();
                                    panelimagen.add(imageLabel, BorderLayout.CENTER);
                                    panelimagen.revalidate();
                                    panelimagen.repaint();
                                } else {
                                    System.out.println("No se pudo leer la imagen desde los bytes proporcionados.");
                                }
                            } else {
                                System.out.println("El arreglo de bytes de la imagen está vacío.");
                            }
                        } catch (Exception ex) {
                            System.out.println("Error al procesar la imagen: " + ex.getMessage());
                            ex.printStackTrace(); // Imprimir la traza de la excepción para depurar
                        }
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Por favor, seleccione una fila para agregar.");
                }
            }
        });

        // Configurar ActionListener para el botón "Salir"
        btnSalir.addActionListener(new ActionListener() {
            public void actionPerformed(ActionEvent e) {
                dialogo.dispose(); // Cerrar el JDialog
            }
        });

        // Crear un panel para contener los botones
        JPanel panelBotones = new JPanel();
        panelBotones.add(btnAgregar);
        panelBotones.add(btnSalir);

        // Agregar el panel de botones al JDialog
        dialogo.add(panelBotones, BorderLayout.SOUTH);

        // Crear el campo de texto para la búsqueda por DNI
        JTextField txtBDNI = new JTextField(16);
        JPanel panelBusqueda = new JPanel();
        panelBusqueda.add(new JLabel("Buscar por Codigo:"));
        panelBusqueda.add(txtBDNI);
        dialogo.add(panelBusqueda, BorderLayout.NORTH);

        // Configurar KeyListener para la búsqueda por DNI
        txtBDNI.addKeyListener(new KeyAdapter() {
            public void keyReleased(KeyEvent e) {
                String dni = txtBDNI.getText();
                TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(modelo);
                tabla.setRowSorter(sorter);
                sorter.setRowFilter(RowFilter.regexFilter(dni, 1)); // Filtrar por la columna DNI
            }
        });

        dialogo.setVisible(true); // Mostrar el JDialog
    }//GEN-LAST:event_btnBusquedaDoctorMouseClicked

