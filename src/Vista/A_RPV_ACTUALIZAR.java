    private void btnActualizarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnActualizarMouseClicked
        String id = txtID.getText();
        String codigo = txtCodigo.getText();
        String empresa = txtEmpresa.getText();
        String ruc = txtRuc.getText();
        String correo = txtCorreo.getText();
        String telefono = txtTelefono.getText();

        if (empresa.isEmpty() || ruc.isEmpty() || correo.isEmpty() || telefono.isEmpty() || selectedImage == null) {
            JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos y seleccione los archivos necesarios.");
            return;
        }

        try {
            String query = "UPDATE registro_proveedor SET codigo_prov=?, empresa=?, RUC=?, telefono=?, correo=?, imagen=? WHERE id=?";
            PreparedStatement pst = cn.prepareStatement(query);
            pst.setString(1, codigo);
            pst.setString(2, empresa);
            pst.setString(3, ruc);
            pst.setString(4, telefono);
            pst.setString(5, correo);

            // Dejar la parte de la imagen tal como está
            FileInputStream fis = new FileInputStream(selectedImage);
            pst.setBinaryStream(6, fis);

            pst.setString(7, id);

            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Datos actualizados correctamente.");
            limpiarEntradas();
        } catch (SQLException | IOException e) {
            JOptionPane.showMessageDialog(this, "Error al actualizar los datos: " + e.getMessage());
        }
    }//GEN-LAST:event_btnActualizarMouseClicked

